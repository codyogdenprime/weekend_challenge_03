var calculate = {
	add: function ( x, y ) {
		return ( Number( x ) + Number ( y ) );
	},
	sub: function ( x, y ) {
		return ( Number( x ) - Number ( y ) );
	},
	mul: function ( x, y ) {
		return ( Number( x ) * Number ( y ) );
	},
	div: function ( x, y ) {
		return ( Number( x ) / Number ( y ) );
	}
};

module.exports = calculate;